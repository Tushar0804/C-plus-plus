#include <iostream>
using namespace std;

int main()
{
	char ch = 'A';
	
	int a = 1456000;
	short b = 95;
	long c = 123456789;
	
	float d = 10.568f;
	double e = 15556.5556669;
	long double f = 102568955.4644335L;
	
	bool g = true;
	
	cout << "Character ch = " << ch << endl;
	cout << "Integer a = " << a << endl;
	cout << "Short Integer b = " << b << endl;
	cout << "Long Integer c = " << c << endl;
	cout << "Floating Point d = " << d << endl;
	cout << "Double e = " << e << endl;
	cout << "Long Double f = " << f << endl;
	cout << "Boolean g = " << g << endl;
	
	return 0;
}
